package com.example.vo;

import com.example.annotation.FieldLog;

/**
 * @Author: liujinhui
 * @Date: 2019/9/25 12:48
 */
public class Student {
    private String id;

    @FieldLog(value = "姓名")
    private String name;
    private Integer age;

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Student{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
